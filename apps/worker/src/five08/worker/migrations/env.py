"""Alembic environment for worker DB schema migrations."""

from __future__ import annotations

import os
from logging.config import fileConfig

from alembic import context
from sqlalchemy import create_engine, pool

config = context.config

if config.config_file_name is not None:
    fileConfig(config.config_file_name)


def _get_url() -> str:
    """Load database URL from Alembic config or environment."""
    url = config.get_main_option("sqlalchemy.url") or os.getenv("POSTGRES_URL")
    if not url:
        raise RuntimeError("POSTGRES_URL is required for worker job migrations.")
    if url.startswith("postgresql://"):
        return url.replace("postgresql://", "postgresql+psycopg://", 1)
    return url


target_metadata = None


def run_migrations_offline() -> None:
    """Run migrations without opening a DB connection."""
    context.configure(
        url=_get_url(),
        target_metadata=target_metadata,
        compare_type=True,
        literal_binds=True,
    )

    with context.begin_transaction():
        context.run_migrations()


def run_migrations_online() -> None:
    """Run migrations using an active SQLAlchemy connection."""
    connectable = create_engine(_get_url(), poolclass=pool.NullPool)

    with connectable.connect() as connection:
        context.configure(
            connection=connection,
            target_metadata=target_metadata,
            compare_type=True,
        )

        with context.begin_transaction():
            context.run_migrations()


if context.is_offline_mode():
    run_migrations_offline()
else:
    run_migrations_online()
